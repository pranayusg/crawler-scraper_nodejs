methods.getHtml = function (url) {
    if (count > 20) {
        count = 0
    }
    else {
        count++
    }
    var defer = Q.defer();
    var headers = {
        'User-Agent': UserAgent[count]
    }

    //Configure the request
    var options = {
        url: url,
        method: 'GET',
        headers: headers,
        pool: false
    }
    setTimeout(function () {
        request(options)
            .then(function (res) {
                defer.resolve(res);
                // fs.writeFileSync('ad.html', res);
            })
            .catch(function (err) {
                //  console.log(err);
                console.log("Error receiving response");
            });
    }, Math.random() * 60);

    return defer.promise;
}

